    // MARK: UICollectionViewDataSource
    var characterProfiles: [CharacterProfile]? {
        didSet {
            if isViewLoaded {
                collectionView.reloadData()
            }
        }
    }
    
    override func numberOfSections(in collectionView: UICollectionView) -> Int {
        return 1
    }
    
    
    override func collectionView(_ collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
        return characterProfiles?.count ?? 0
    }
    
    override func collectionView(_ collectionView: UICollectionView, cellForItemAt indexPath: IndexPath) -> UICollectionViewCell {
        let cell = collectionView.dequeueReusableCell(withReuseIdentifier: reuseIdentifier, for: indexPath) as! CharCollectionViewCell
        cell.backgroundColor = .black
        
        if let characterProfile = characterProfiles?[indexPath.row] {
            CharacterProfile.getImageFromWeb(characterProfile.Icon.URL) { (dlImg) in
                if let image = dlImg {
                    cell.charImg.image = image
                } else { // if you use an Else statement, it will be in background
                    cell.charImg.image = UIImage(named: "placeholder")
                }
            }
        }
        
        return cell
    }
    
    // MARK: - Segues
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        if segue.identifier == "collectionShowDetail" {
            if let indexPath = collectionView.indexPathsForSelectedItems?.first {
                if let characterProfile = characterProfiles?[indexPath.row] {
                    let controller = (segue.destination as! UINavigationController).topViewController as! DetailViewController
                    controller.detailItem = characterProfile
                }
            }
        }
    }
    
